        if(nk>0):
            for k in range(nk):
                if(tt>stoplimit):
                    Rt[j]=0.001
                ni = np.random.poisson(Rt[j]) ### how many people will be infected by this existing virus carrier person.
                a_list=[0,1]
                distribution=[1-pp,pp]
                imuind = random.choices(a_list, distribution) 

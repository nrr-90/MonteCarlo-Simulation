        imuind = random.choices(a_list, distribution)
        if(imuind == 1):
            ni=0
        tt=tt+ni  
        if(ni>0):
            tk=  np.repeat(0,ni)
            for i in range(ni):
                tk[i]= np.random.negative_binomial(1,(size/(size + mut)),size)+1 ##this is the nth day on which a new case occurs
                kk[tk[i]]= kk[tk[i]]+1
            pastevent = [*np.repeat(1,max(tk)-1),*np.repeat(0,(days-max(tk)+1))]
            #pastevent.tolist()
